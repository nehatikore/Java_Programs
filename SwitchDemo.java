class SwitchDemo {
 
   public static void main(String[] args) 
       {
        
             int i=2;
        
            switch(i)
   
             {
            
                  case 1:
            
                         System.out.println("First Choice");
  
                         break;
         
                 case 2:
                
                         System.out.println("Second Choice");
   
                         break;
 
               case 3:
   
                        System.out.println("Third Choice");
   
                        break;
           
               default:
          
                       System.out.println("Invalid Choice");
   
             }
  
        }

}